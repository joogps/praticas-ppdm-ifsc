package com.joogps.lista;

public class ItemAlbum {
    int id;
    String nome;
    String artista;
    String ano;
    int capa;

    ItemAlbum(int id, String nome, String artista, String ano, int capa) {
        this.id = id;
        this.nome = nome;
        this.artista = artista;
        this.ano = ano;
        this.capa = capa;
    }
}
